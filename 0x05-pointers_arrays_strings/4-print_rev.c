void print_rev(char *s)
{
    int i;
    for (i = 0; s[i] != '\0'; i++)
    {
        continue;
    }
    int n;
    int c = i;
    char *str;
    str[c + 1];

    for ( n = 0; n == (c + 1) ; n++)
    {
        if (n <= c)
        {
         str[n] = s[i];
        }
        else if (n > c)
        {
            str[n+1] = '\0';
        }
        i--;
       
    }
    
}
